    </div>
</div>


<div class="row">
    <div class="col-12">
        <br><p class="text-justify">O tráfego japonês era intenso, sendo boa parte rotineira e nem todas mensagens eram decifradas. Examinando origem, destino e tamanho da mensagem os analistas de tráfego percebiam quais eram importantes, se houvesse dúvida a mensagem era parcialmente decifrada. </p>
        <p class="text-justify">Em maio de 1942, o tráfego de mensagens japonesas aumentou significativamente, indicando que planejavam algo.</p>
        <p class="text-justify">A estação Hypo, já construíra cerca de um terço do libro de códigos JN-25 e conseguia ler 90% das mensagens interceptadas. </p><br>
    </div>
</div>

<div class="row">
    <div class="col-12">
        <br><h5 style="color: #8cc43d">Midway</h5><br>
    </div>
</div>

<div class="row">
    <div class="col-12">
        <p class="text-justify">Em maio de 1942, Rochefort encontrou as palavras koryaku butai - força de invasão - numa mensagem interceptada, seguida pela localização AF.</p>
        <p class="text-justify">Parecia que o almirante japonês Isoroku Yamamoto queria atrair a frota remanescente do pacífico para Midway, onde pretendia destruir a base.</p>
        <p class="text-justify">Os americanos perceberam que se chegassem antes a Midway, conseguiriam virar a mesa, mas precisavam ter certeza de que AF era realmente Midway ou correriam o risco de deixar o Havaí desprotegido. </p>
        <p class="text-justify">A unidade OP-2-G, em Washington, afirmou que a unidade Hypo havia errados as tabelas, na verdade não era AF, mas AG – a ilha Johnson.</p>
        <p class="text-justify">Rochefort fez um plano, mandou uma mensagem avisando que a usina de salinização da ilha estava com defeito, a mensagem foi captada pelos japoneses e dois dias depois uma estação americana interceptou uma mensagem no código JN-25, dizendo que AF estava com pouca água doce, confirmando que AF era Midway.</p>
        <p class="text-justify">Uma mensagem decifrada anteriormente mostrara que o ataque aéreo da ilha viria do oeste, agora Rochefort descobrira que começaria em 3 de junho. Foi um golpe de sorte, pois no memo dia os japoneses trocaram os livros de códigos e as tabelas aditivas. </p>
        <p class="text-justify">Porta-aviões foram enviados, ao norte uma força de cruzadores e contratorpedeiros protegia os flancos e os americanos também tinham a vantagem dos aviões estacionados em Midway e no Havaí. Os japoneses não tinham apoio em terra.</p>
        <p class="text-justify">Quando a batalha começou,  bombardeiros americanos atacaram os porta-aviões japoneses, que ainda estavam a 350 km a sudoeste da frota americana, a luta durou quatro dias.</p>
        <p class="text-justify">Essa foi uma vitória decisiva para os Estados Unidos, pois a partir desse momento a marinha japonesa não tinha mais superioridade na guerra. O avanço japonês no Pacífico foi interrompido, bastando aos orientais a retirada.</p><br>
