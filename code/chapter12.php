        <h2 style="color: #8cc43d">Capítulo 12 - A Criptologia Volta a Atacar</h2>
    </div>
</div>

<div class="row">
    <div class="col-12">
        <br><p class="text-justify"><i> “Posso oferecer muitas desculpas, mas fracassamos em nossa tarefa. Um oficial de informações tem um serviço, uma tarefa, uma missão: dizer a seu comandante, a seu superior, hoje, o que os japoneses farão amanhã.” </i>
        <p class="text-right"> <b> Capitão-de-corveta Joseph J. Rochefort </b>, comandante de informações em Combate no Havai </p>
        <p class="text-justify"> Disseram ao povo americano que o ataque surpresa a Pearl Harbor foi um “fracasso do serviço de informações”. Os decifradores do Havaí e de Washington estavam decididos a não levar a culpa por outra catástrofe. Seis meses depois, eles boram responsávei pela vitória em Midway, que virou a guerra do Pacífico a favor dos Estados Unidos.</p>
    </div>
</div>

<div class="row">
    <div class="col-12">
        <br><h5 style="color: #8cc43d">Estação Hypo</h5>
    </div>
</div>

<div class="row">
    <div class="col-12">
        <br><p class="text-justify">A Unidade de Informações em Combate no Havaí, chamada de Estação Hypo, estava alocada em um imenso porão, chamado de Masmora, ideal para manter as máquinas IBM, que processavam três milhões de cartões perfurados por mês a salvo do calor tropical.</p>
        <p class="text-justify">Seu principal criptoanalista, capitão-de-corveta Thomas H. Dyer, cumpria turnos de até 48 horas, quando sua equipe de decifradores começou a entender os princípios básicos para decifra o JN-25, código de criptografia japonês. O comandante Rochefort contratou todos os falantes de japonês, pois decifrar o código, parecia uma questão de força de trabalho e o efetivo da equipe logo subiu para 120 pessoas, lendo de quinhentos a mil despachos por dia.</p>
    </div>
</div>


<div class="row">
    <div class="col-12">
        <br><h5 style="color: #8cc43d">Novo Livro de Códigos</h5>
    </div>
</div>

<div class="row">
    <div class="col-12">
        <br><p class="text-justify">Em abril de 1942, os japoneses lançaram o Livro de Códigos Navais D, chamado pelos americanos de JN-25c, por problemas em distribuição os japoneses continuaram usando a versão anterior até maio, o que permitiu aos americanos decifrar mensagens que os japoneses planejavam tomar Porto Moresby na Papua Nova Guiné. </p>
        <p class="text-justify">Os americanos enviaram dois porta-aviões, o resultado foi a Batalha do Mar de Coral, a 1ª batalha naval entre frotas inimigas no Pacífico e a 1ª batalha em que as frotas não estavam ao alcance visual uma da outra, já que foi travada inteiramente por aviões. </p>
        <p class="text-justify">Houveram pesadas baixas de ambos os lados, mas os japoneses ficaram com pouquíssimos aviões para proteger os desembarques na Nova Guiné. </p>
        <p class="text-justify">Essa batalha foi uma vitória dos criptoanalistas americanos, que continuaram trabalhando 24 horas por dia e sempre que um código era decifradom os dados revelados eram perfurados em cartões IBM e armazenados na máquina.</p>
        <p class="text-justify">O código JN-25 era supercifrado, números eram acrescentados à lista de núeros codificados para criptografar ainda mais a mensagem. Para descriptografar faziam-se subtrações na busca de um resto em comum, força cerebral humana era necessária para reconstruir a sequência adicionada. Os códigos decifrados do superciframento, mas ainda não decodificados podiam então ser comparados com as codificações armazenadas, fazendo contagens de frequência e uso de outros métodos estatísticos, como procurar justaposição de grupos de códigos. </p> <br>
    </div>
</div>

