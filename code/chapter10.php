	<div class="col-12 col-md-12"> 
		<p class="text-justify">A Purple Machine era composta de duas máquinas de escrever e um sistema de rotor elétrico com um quadro de distribuição alfabético de 25 caracteres. Como a Enigma Machine, a primeira máquina de escrever foi o método no qual o texto simples, ou mensagem não criptografada, poderia ser inserido manualmente. A máquina de escrever foi construída para ser compatível com inglês, romaji e romano, adicionando um nível de mistério pela escolha do idioma.</p>
		<p class="text-justify">Então em meados de 1939 chamada máquina PURPLE em si foi usado pela primeira vez pelo Japão, a agência EUA através da sua agência (SIS) Serviço de informações em mensagens com seus criptógrafos tinham quebrado algum tráfego da máquina de criptografar japonesa, essa nova máquina usava os mesmos padrões de codificação da então maquina vermelha, combinações do alfabeto cifrados com vogais não cifradas. As máquinas roxas foram utilizadas apenas para transportar o tráfego diplomático às suas embaixadas. A agência americana SIS então chamou esse novo padrão de codificação de JN-25, nome esse que faz referência ao vigésimo quinto sistema identificado da marinha Japonesa.</p><br>
